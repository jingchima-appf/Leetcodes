package bitAndIntegers;

public class BitAndInteger {

    public static void main(String[] args) {
        Integer a = -1;
        Integer b = -1;
        System.out.println(a == b);
        String c = "good";
        String d = "good";
        System.out.println(c == d);
        Integer e = null;
        System.out.println(e + "");
        String s = "11";
        System.out.println(Integer.parseInt(s));
    }
}
